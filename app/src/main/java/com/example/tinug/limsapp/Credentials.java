public class Credentials {

    int id;
    String username, password;

    public void setId()
    {
        this.id= id;
    }

    public int getId()
    {
        return id;
    }

    public void setUsername()
    {
        this.username= username;
    }

    public String getUsername()
    {
        return username;
    }


    public void setPassword()
    {
        this.password= password;
    }

    public String getPassword()
    {
        return password;
    }


}
